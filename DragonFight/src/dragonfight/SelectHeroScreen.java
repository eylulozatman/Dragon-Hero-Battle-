    public boolean createhero()
    {
        
       String heroname = nametxt.getText();
      
    
       if(selectWeapon() && selectArmor())
       {
          hero = new Hero(heroname,weapon,armor);
          hero.setName(heroname);
          hero.setArmor(armor);
          hero.setWeapon(weapon);
         return true;
       }
        
       else
       {
           return false;
       }
    
       
    }
