    private boolean selectWeapon() {
       
        
        if(swordbx.isSelected() && (!hammerbx.isSelected()) )
       {
           weapon = new sword();
           weapon.CreateWeapon();
           return true;
            
       }
       else if(hammerbx.isSelected() && (!swordbx.isSelected()))
       {
           weapon = new hammer();
           weapon.CreateWeapon();
           return true;
       }
       else
       {
            JOptionPane.showMessageDialog(this,"your weapon choose is incorrect");
           return false;
       }
       
        
    }

    private boolean selectArmor() 
    {
        if(goldbx.isSelected() && (!shinybx.isSelected()))
       {
           armor = new GoldArmor();
           armor.createArmor();
           return true;
           
       }
       else if(shinybx.isSelected() && (!goldbx.isSelected()))
       {
           armor = new ShinyArmor();
           armor.createArmor();
           return true;
       }
        else
       {
            JOptionPane.showMessageDialog(this,"your armor choose is incorrect");
             return false;
       }
       
    }
